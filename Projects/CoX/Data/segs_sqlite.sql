CREATE TABLE accounts
(
  id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
  username VARYING CHARACTER(32) UNIQUE,
  access_level SMALLINT,
  creation_date TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP NOT NULL,
  passw BYTEA
);

CREATE TABLE bans
(
  id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT REFERENCES accounts(id) ON UPDATE CASCADE ON DELETE CASCADE,
  account_id INTEGER,
  offending_ip INET,
  started TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  reason text
);

CREATE TABLE game_servers
(
  id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
  addr INET,
  port SMALLINT,
  name VARYING CHARACTER(100),
  token BIGINT
);
